#!/usr/bin/env node
/**
 * Read each lookup-bench JSONL file, compute median latency (ns) over the
 * per-bitmap sample arrays for the hit ("found") and miss ("not_found")
 * scenarios per (n, p), and emit a single TS module with one grid per
 * (distribution, scenario) combination.
 *
 * Output shape per scenario:
 *   { id, label, n[], p[], kMean, roaring: { hit: { nsMedian }, miss: { nsMedian } } }
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..');
const DATA_DIR = join(REPO_ROOT, 'data', 'lookup-bench');
const OUT_FILE = join(REPO_ROOT, 'src', 'data', 'lookupLatency.ts');

const SCENARIOS = [
  { id: 'uniform',      file: 'uniform_lookup.jsonl',      label: 'Uniform' },
  { id: 'zipf-0.9',     file: 'zipf_s0.9_lookup.jsonl',    label: 'Zipf (s=0.9)' },
  { id: 'zipf-1.0',     file: 'zipf_s1.0_lookup.jsonl',    label: 'Zipf (s=1.0)' },
  { id: 'zipf-1.2',     file: 'zipf_s1.2_lookup.jsonl',    label: 'Zipf (s=1.2)' },
  { id: 'block-c8',     file: 'blockC8_lookup.jsonl',      label: 'Block (c=8)' },
  { id: 'block-c64',    file: 'blockC64_lookup.jsonl',     label: 'Block (c=64)' },
  { id: 'block-c1024',  file: 'blockC1024_lookup.jsonl',   label: 'Block (c=1024)' },
];

// Median is more robust than mean against OS jitter tails. Each row already
// aggregates ~300 samples, so the median is well-defined.
function median(values) {
  if (values.length === 0) return null;
  const sorted = [...values].sort((a, b) => a - b);
  const mid = sorted.length >> 1;
  return sorted.length % 2 ? sorted[mid] : (sorted[mid - 1] + sorted[mid]) / 2;
}

function mean(values) {
  if (values.length === 0) return null;
  let s = 0;
  for (const v of values) s += v;
  return s / values.length;
}

function parseRows(path) {
  const text = readFileSync(path, 'utf8');
  return text
    .split('\n')
    .filter(Boolean)
    .map((line) => JSON.parse(line))
    .filter((row) => row.type === 'row');
}

/**
 * Build axes and hit/miss grids from a single library's JSONL.
 * Rows collapse samples across all reps (found_ns / not_found_ns already
 * concatenate samples from 3 reps × ~99 lookups each).
 */
function loadSide(path) {
  const rows = parseRows(path);
  const byN = new Map();
  const ns = new Set();
  const ps = new Set();

  for (const row of rows) {
    const { n, p } = row;
    ns.add(n);
    ps.add(p);
    if (!byN.has(n)) byN.set(n, new Map());
    const byP = byN.get(n);
    if (!byP.has(p)) byP.set(p, { found: [], notFound: [], k: [] });
    const cell = byP.get(p);
    if (Array.isArray(row.found_ns)) cell.found.push(...row.found_ns);
    if (Array.isArray(row.not_found_ns)) cell.notFound.push(...row.not_found_ns);
    if (typeof row.mean_k === 'number') cell.k.push(row.mean_k);
  }

  const nValues = [...ns].sort((a, b) => a - b);
  const pValues = [...ps].sort((a, b) => a - b);
  const makeGrid = () => pValues.map(() => new Array(nValues.length).fill(null));
  const hit = makeGrid();
  const miss = makeGrid();
  const kMean = makeGrid();

  for (let pi = 0; pi < pValues.length; pi++) {
    for (let ni = 0; ni < nValues.length; ni++) {
      const cell = byN.get(nValues[ni])?.get(pValues[pi]);
      if (!cell) continue;
      const h = median(cell.found);
      const m = median(cell.notFound);
      const k = mean(cell.k);
      if (h != null) hit[pi][ni] = +h.toFixed(2);
      if (m != null) miss[pi][ni] = +m.toFixed(2);
      if (k != null) kMean[pi][ni] = +k.toFixed(2);
    }
  }

  return { n: nValues, p: pValues, kMean, hit, miss };
}

const scenarios = SCENARIOS.map((meta) => {
  const roaring = loadSide(join(DATA_DIR, meta.file));
  return {
    id: meta.id,
    label: meta.label,
    n: roaring.n,
    p: roaring.p,
    kMean: roaring.kMean,
    roaring: { hit: { nsMedian: roaring.hit }, miss: { nsMedian: roaring.miss } },
  };
});

const header = `// AUTO-GENERATED by scripts/build-lookup-latency-data.mjs — do not edit by hand.
// Source: data/lookup-bench/*_lookup.jsonl. Regenerate via
//   \`node scripts/build-lookup-latency-data.mjs\`.

/** Per-[p][n] grid of averaged values (null = missing data point). */
export type LookupGrid = (number | null)[][];

export type LookupScenarioId = 'hit' | 'miss';

export interface LookupLatencyGrids {
  /** Median latency in nanoseconds, indexed [p][n]. */
  nsMedian: LookupGrid;
}

export interface LookupLatencyScenario {
  id: string;
  label: string;
  /** Universe size axis values (ascending). */
  n: number[];
  /** Bitmap density-percent axis values (ascending, 0..1). */
  p: number[];
  /** Mean measured cardinality of the bitmap, indexed [p][n]. */
  kMean: LookupGrid;
  /** Per-scenario latency grids (Roaring). */
  roaring: Record<LookupScenarioId, LookupLatencyGrids>;
}
`;

const body = `\nexport const LOOKUP_LATENCY_SCENARIOS: LookupLatencyScenario[] = ${JSON.stringify(
  scenarios,
  null,
  2,
)};\n`;

writeFileSync(OUT_FILE, header + body);

console.log(`Wrote ${OUT_FILE}`);
console.log(`Scenarios: ${scenarios.map((s) => s.id).join(', ')}`);
const s0 = scenarios[0];
console.log(`Grid: ${s0.p.length} p × ${s0.n.length} n`);

const tally = (grids) => {
  const hitCount = grids.hit.nsMedian.reduce(
    (acc, row) => acc + row.filter((v) => v != null).length,
    0,
  );
  const missCount = grids.miss.nsMedian.reduce(
    (acc, row) => acc + row.filter((v) => v != null).length,
    0,
  );
  return `hit=${hitCount} miss=${missCount}`;
};
for (const s of scenarios) {
  console.log(`  ${s.id.padEnd(12)} roaring(${tally(s.roaring)})`);
}
