#!/usr/bin/env node
// Read each size-bench JSONL file, average bytes over reps per (n, p),
// and emit a single TS module with compact per-scenario grids.
// x = universe size (n), y = cardinality % (p), z = optimized bytes (averaged).

import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..');
const DATA_DIR = join(REPO_ROOT, 'data', 'size-bench');
const OUT_FILE = join(REPO_ROOT, 'src', 'data', 'sizeBench.ts');

const SCENARIOS = [
  { id: 'uniform',      file: 'uniform_results.jsonl',      label: 'Uniform' },
  { id: 'zipf-0.9',     file: 'zipf_s0.9_results.jsonl',    label: 'Zipf (s=0.9)' },
  { id: 'zipf-1.0',     file: 'zipf_s1.0_results.jsonl',    label: 'Zipf (s=1.0)' },
  { id: 'zipf-1.2',     file: 'zipf_s1.2_results.jsonl',    label: 'Zipf (s=1.2)' },
  { id: 'block-c8',     file: 'block_c8_results.jsonl',     label: 'Block (c=8)' },
  { id: 'block-c64',    file: 'block_c64_results.jsonl',    label: 'Block (c=64)' },
  { id: 'block-c1024',  file: 'block_c1024_results.jsonl',  label: 'Block (c=1024)' },
];

// Fields we pull from each `stats` block. Keys match the JSONL schema.
const STAT_FIELDS = [
  'n_containers',
  'n_array_containers',
  'n_run_containers',
  'n_bitset_containers',
  'n_values_array_containers',
  'n_values_run_containers',
  'n_values_bitset_containers',
  'n_bytes_array_containers',
  'n_bytes_run_containers',
  'n_bytes_bitset_containers',
];

function emptyAccum() {
  const acc = { sumOpt: 0, sumUnopt: 0, sumK: 0, count: 0 };
  for (const f of STAT_FIELDS) {
    acc[`opt_${f}`] = 0;
    acc[`unopt_${f}`] = 0;
  }
  return acc;
}

function loadScenario(path) {
  const text = readFileSync(path, 'utf8');
  const lines = text.split('\n').filter(Boolean);

  // Sum + count by (n, p) so we average reps.
  const byN = new Map();
  const ns = new Set();
  const ps = new Set();

  for (const line of lines) {
    const row = JSON.parse(line);
    if (row.type !== 'row') continue;
    const { n, p } = row;
    const optBytes = row.optimized?.bytes ?? 0;
    const unoptBytes = row.unoptimized?.bytes ?? 0;
    const optStats = row.optimized?.stats ?? {};
    const unoptStats = row.unoptimized?.stats ?? {};
    const k = row.k ?? 0;

    ns.add(n);
    ps.add(p);

    if (!byN.has(n)) byN.set(n, new Map());
    const byP = byN.get(n);
    if (!byP.has(p)) byP.set(p, emptyAccum());
    const cell = byP.get(p);
    cell.sumOpt += optBytes;
    cell.sumUnopt += unoptBytes;
    cell.sumK += k;
    cell.count += 1;
    for (const f of STAT_FIELDS) {
      cell[`opt_${f}`] += optStats[f] ?? 0;
      cell[`unopt_${f}`] += unoptStats[f] ?? 0;
    }
  }

  const nValues = [...ns].sort((a, b) => a - b);
  const pValues = [...ps].sort((a, b) => a - b);

  // z[p_index][n_index] — plotly's convention for surfaces.
  const makeGrid = () =>
    pValues.map(() => new Array(nValues.length).fill(null));
  const optBytes = makeGrid();
  const unoptBytes = makeGrid();
  const cardinality = makeGrid();

  // Per-field averaged grids for optimized and unoptimized stats.
  const optStatsGrids = Object.fromEntries(
    STAT_FIELDS.map((f) => [f, makeGrid()]),
  );
  const unoptStatsGrids = Object.fromEntries(
    STAT_FIELDS.map((f) => [f, makeGrid()]),
  );

  for (let pi = 0; pi < pValues.length; pi++) {
    for (let ni = 0; ni < nValues.length; ni++) {
      const cell = byN.get(nValues[ni])?.get(pValues[pi]);
      if (!cell) continue;
      optBytes[pi][ni] = +(cell.sumOpt / cell.count).toFixed(2);
      unoptBytes[pi][ni] = +(cell.sumUnopt / cell.count).toFixed(2);
      cardinality[pi][ni] = +(cell.sumK / cell.count).toFixed(2);
      for (const f of STAT_FIELDS) {
        optStatsGrids[f][pi][ni] = +(cell[`opt_${f}`] / cell.count).toFixed(2);
        unoptStatsGrids[f][pi][ni] = +(cell[`unopt_${f}`] / cell.count).toFixed(2);
      }
    }
  }

  return {
    n: nValues,
    p: pValues,
    optBytes,
    unoptBytes,
    cardinality,
    optStats: optStatsGrids,
    unoptStats: unoptStatsGrids,
  };
}

const scenarios = SCENARIOS.map((meta) => {
  const grid = loadScenario(join(DATA_DIR, meta.file));
  return {
    id: meta.id,
    label: meta.label,
    ...grid,
  };
});

// Emit a TS module.
const header = `// AUTO-GENERATED by scripts/build-size-bench-data.mjs — do not edit by hand.
// Source: data/size-bench/*_results.jsonl. Regenerate via \`node scripts/build-size-bench-data.mjs\`.

/** Per-[p][n] grid of averaged values (null = missing data point). */
export type Grid = (number | null)[][];

/**
 * Averaged roaring internal stats, split by container type. Each field is a
 * [p][n] grid matching the scenario's axes.
 */
export interface ScenarioStats {
  /** Total containers. */
  n_containers: Grid;
  /** Container counts, by container type. */
  n_array_containers: Grid;
  n_run_containers: Grid;
  n_bitset_containers: Grid;
  /** Total values stored, by container type. */
  n_values_array_containers: Grid;
  n_values_run_containers: Grid;
  n_values_bitset_containers: Grid;
  /** Bytes used, by container type. */
  n_bytes_array_containers: Grid;
  n_bytes_run_containers: Grid;
  n_bytes_bitset_containers: Grid;
}

export interface ScenarioGrid {
  id: string;
  label: string;
  /** Universe size axis values (ascending). */
  n: number[];
  /** Cardinality-percent axis values (ascending, 0..1). */
  p: number[];
  /** Averaged optimized bytes, indexed [p][n]. null = missing data point. */
  optBytes: Grid;
  /** Averaged unoptimized bytes, indexed [p][n]. */
  unoptBytes: Grid;
  /** Averaged cardinality (count of set values), indexed [p][n]. */
  cardinality: Grid;
  /** Averaged roaring internal stats after run-optimization. */
  optStats: ScenarioStats;
  /** Averaged roaring internal stats before run-optimization. */
  unoptStats: ScenarioStats;
}
`;

const body = `\nexport const SIZE_BENCH_SCENARIOS: ScenarioGrid[] = ${JSON.stringify(
  scenarios,
  null,
  2,
)};\n`;

writeFileSync(OUT_FILE, header + body);
console.log(`Wrote ${OUT_FILE}`);
console.log(`Scenarios: ${scenarios.map((s) => s.id).join(', ')}`);
console.log(`Grid: ${scenarios[0].p.length} p × ${scenarios[0].n.length} n`);
